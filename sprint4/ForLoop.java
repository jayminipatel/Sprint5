package sprint4;

public class ForLoop {
    public static void main(String[] args) {
        //Create multiples of 3
        for (int a=3;a<=30;a+=3){
            System.out.println(a);
        }
        //Create 10 even numbers
        for (int b=0;b<=20;b++){
            System.out.println(b++);
        }
        //Create able of 3
        for (int c=0;c<=10;c++){
            System.out.println("3*"+c+"="+3*c);

        }
    }
    }

