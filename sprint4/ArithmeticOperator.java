package sprint4;

public class ArithmeticOperator {

    public static void main(String[] args) {
        int a= 14;
        int b= 25;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);
    }
}
