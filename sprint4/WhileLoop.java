package sprint4;

public class WhileLoop {
    public static void main(String[] args) {
        //Create multiples of 3 until number 20.
       int a=3;
       while(a<=20){
           System.out.println(a);a+=3;

       }
       //Create first 5 odd numbers.
       int b=1;
       while(b<=10){
           System.out.println(b);b+=2;

       }
    }
}
