package sprint4;

public class UnaryOperators {
    public static void main(String[] args) {
        int a= 11;
        boolean b= true;
        System.out.println(a++);
        System.out.println(++a);
        System.out.println(a--);
        System.out.println(--a);
        System.out.println(!b);

    }
}
